var solveNQueens = function(n) { 
    const res = [];
    backtrack(res, n);
    return res;
};

function backtrack(res, n, board = [], r = 0) {
    if (r === n) {
        res.push(board.map(c => '.'.repeat(c) + 'Q' + '.'.repeat(n - c - 1))); // è®°å½•ä¿¡æ¯
        return;
    }
    for (let c = 0; c < n; c++) {
        if (!board.some((bc, br) => bc === c || bc === c + r - br || bc === c - r + br)) { // ä»»ä¸€æ»¡è¶³ï¼Œå›è°ƒæ‰§è¡Œ
            board.push(c);
            backtrack(res, n, board, r + 1);
            board.pop();
        }
    }
}
