# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution:
    """
    方法一、暴力法，合并两个链表，并且对链表进行排序O(nlogn)
    方法二、递归的思想
    """
    def mergeTwoLists(self, l1: ListNode, l2: ListNode) -> ListNode:
        # 递归结束条件
        if l1 is None:
            return l2
        elif l2 is None:
            return l1

        if l1.val < l2.val:
            l1.next = self.mergeTwoLists(l1.next, l2)
            return l1
        else:
            l2.next = self.mergeTwoLists(l1, l2.next)
            return l2